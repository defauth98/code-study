// Métodos do array

const frutas = ['Maçã', 'Banana'];

frutas.push('Laranja') // Adiciona ao final 

frutas.pop() // Remove do final 

frutas.shift() // Remove do inicio 

frutas.unshift('Goiaba') // Adiciona ao inicio

frutas.indexOf('Maçã') // Retorna o index do elemento

frutas.splice(0, 1) // Remove um item do array